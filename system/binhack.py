def get_application_path() -> str:
    """Get the application path whether running as script or frozen executable"""
    if getattr(sys, 'frozen', False):
        # Running as compiled executable
        return os.path.dirname(sys.executable)
    else:
        # Running as script
        return os.path.dirname(os.path.abspath(__file__))

def get_file_size(f: BinaryIO) -> int:
    """Get the size of a file"""
    current_pos = f.tell()
    f.seek(0, 2)  # Seek to end
    size = f.tell()
    f.seek(current_pos, 0)  # Restore position
    return size

def is_wince(boot_file: BinaryIO, hack_offset: int) -> bool:
    """Check if the boot file is Windows CE"""
    if hack_offset == -1:
        return False
    boot_file.seek(hack_offset)
    wince_data = boot_file.read(BOOT_HACK_WINCE_CHECK_SIZE)
    boot_file.seek(0)
    return wince_data == WINCE_CHECK_REF

def is_bincon(boot_file: BinaryIO) -> bool:
    """Check if the boot file is bincon'd"""
    boot_file.seek(0)
    bincon_data = boot_file.read(BOOT_HACK_BINCON_CHECK_SIZE)
    boot_file.seek(0)
    return bincon_data == BINCON_CHECK_REF

def search_hack_offset(boot_file: BinaryIO, boot_size: int) -> int:
    """Search for the CD001 offset in the boot file"""
    boot_file.seek(0)
    boot_data = boot_file.read(boot_size)
    
    # Search for the CD001 signature
    for i in range(boot_size - BOOT_HACK_SIGNATURE_SIZE):
        if boot_data[i:i+BOOT_HACK_SIGNATURE_SIZE] == BOOT_SIGN_REF:
            return i - 8  # Return the position 8 bytes before the signature
    
    return -1  # Not found

def hack_katana_boot_binary(boot_file: BinaryIO, hack_offset: int, lba: int, output_path: str) -> bool:
    """Hack the Katana boot binary with the new LBA and save to output path"""
    if hack_offset == -1:
        return False
    
    # Create a copy of the original file
    boot_file.seek(0)
    boot_data = boot_file.read()
    
    # Modify the LBA in the copy
    boot_data = bytearray(boot_data)
    boot_data[hack_offset:hack_offset+4] = struct.pack('<I', lba)
    
    # Write the modified copy to output directory
    with open(output_path, 'wb') as out_file:
        out_file.write(boot_data)
    
    return True

def hack_bootstrap(iphak_file: BinaryIO, boot_size: int, boot_file: BinaryIO, quiet: bool = False):
    """Hack the IP.BIN bootstrap"""
    # Set all region flags
    iphak_file.seek(BOOTSECTOR_HACK_REGION_FLAGS_OFFSET)
    iphak_file.write(BOOTSECTOR_REGION_FLAGS)
    
    # Enable VGA
    iphak_file.seek(BOOTSECTOR_HACK_VGA_OFFSET)
    iphak_file.write(BOOTSECTOR_VGA_FLAG)
    
    # Set OS flag if bincon'd
    if is_bincon(boot_file):
        iphak_file.seek(BOOTSECTOR_HACK_OS_OFFSET)
        iphak_file.write(BOOTSECTOR_OS_FLAG)
        if not quiet:
            print("Bincon detected, OS flag set to 0.")
    
    # Write bootstrap hack data
    iphak_file.seek(BOOTSECTOR_HACK_OFFSET)
    iphak_file.write(BOOTSECTOR_HACK_DATA)
    
    # Write boot size to IP.BIN
    iphak_file.seek(BOOTSECTOR_HACK_BOOTSIZE_OFFSET)
    iphak_file.write(struct.pack('<I', boot_size))

